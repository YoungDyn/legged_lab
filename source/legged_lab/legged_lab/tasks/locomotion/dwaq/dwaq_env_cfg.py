"""Base environment configuration for DreamWAQ (DWAQ) locomotion.

DWAQ is a blind locomotion algorithm that combines PPO with a β-VAE context
encoder. The encoder infers velocity and a latent state from an observation
history, so the actor does **not** receive direct velocity measurements. The
critic uses privileged observations (including velocity).

Observation groups provided to the DWAQ runner / algorithm:
    - ``policy``:      proprioceptive observations for the actor (no velocity).
    - ``critic``:      privileged observations including velocity.
    - ``obs_history``: same terms as policy with ``history_length`` for the VAE.
    - ``velocity``:    root linear velocity used as the VAE supervision target.
"""

